void TMP_I2C_Init(void)
{
	uint8_t temp_data[3];

	temp_data[0]=0X01;//CONFIG REG
	temp_data[1]=0X02;//
	temp_data[2]=0X38;//THERM MODE + H ACTIVE
	HAL_I2C_Master_Transmit(&hi2c2,TMP117_2,temp_data,3,10);//PNP HEATER SENSOR
	HAL_I2C_Master_Transmit(&hi2c2,TMP117_1,temp_data,3,10);//THERMOCOUPLE SENSOR

	temp_data[0]=0X02;//TMP H REG
	temp_data[1]=0X16;//MAX TMP 45
	temp_data[2]=0X80;//MAX TMP 45
	HAL_I2C_Master_Transmit(&hi2c2,TMP117_2,temp_data,3,10);//PNP HEATER MAX 45

	temp_data[0]=0X03;//TMP L REG
	temp_data[1]=0X15;//LOW TMP 42
	temp_data[2]=0X00;//LOW TMP 42
	HAL_I2C_Master_Transmit(&hi2c2,TMP117_2,temp_data,3,10);//PNP HEATER RESUME 42

}

